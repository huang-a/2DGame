package Utilities;

/** A positioning class for everything in the game. */
public class Position {

	/********************
	*					*
	*	  VARIABLES		*
	*					*
	*********************/

	/** The x position. */
	public int X;

	/** The y position. */
	public int Y;
	



	/********************
	*					*
	*	 CONSTRUCTOR	*
	*					*
	*********************/

	public Position(int x, int y) {
		this.X = x;
		this.Y = y;
	}



}